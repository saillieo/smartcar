/**
  * @brief  This function handles SysTick Handler.
  * @param  None
  * @retval None
  */




void SysTick_Handler(void)
{
	int PWM_out;
	
	//1. collect encoder & MPU6050 information
	Encoder_Left=-Read_Speed(3);		//motor is installed at opposite direction, need to add '-'
	Encoder_Right=Read_Speed(4);
	
	mpu_dmp_get_data(&Pitch, &Roll, &Yaw);
	MPU_Get_Gyroscope(&gyrox, &gyroy, &gyroz);
	MPU_Get_Accelerometer(&aacx, &aacy, &aacz);
	
	//2. calculate the control output value
	Velocity_out=Velocity(Target_Speed,Encoder_Left,Encoder_Right);
	Vertical_out=Vertical(Velocity_out+Med_Angle,Roll,gyrox);
	Turn_out=Turn(gyroz);

	PWM_out=Vertical_out;
	
	//3. load the output to motor
	MOTO1=PWM_out-Turn_out;
	MOTO2=PWM_out+Turn_out;
	Limit(&MOTO1,&MOTO2);
	Load(MOTO1, MOTO2);
}

